package fr.mowitnow.mower;

/**
 * @author Brehima
 *
 */
public class Grass {
	
	private int xLength;
	
	private int yLength;
	
	public Grass(int xLength, int yLength) {
		this.xLength = xLength;
		this.yLength = yLength;
	}
	
	public int getXLength() {
		return xLength;
	}
	public int getYLength() {
		return yLength;
	}
	
}
